    <h2>Online Shopping Carts</h2>
<p>By browsing the internet, consumers are able to purchase just about anything on the web and have it shipped right to them. Through online shopping carts, businesses are able to sell to customers with more ease than ever before. Having an online store for your business is crucial in this day and age because so many people are relying on the internet to do most of their shopping. It is estimated that by 2020, ecommerce revenues will reach over 27 trillion dollars. </p>


<p>The four different types of ecommerce include:</p>
<ol>
<li>Business to Consumer (B2C)</li>
<li>Business to Business (B2B)</li>
<li>Consumer to Consumer (C2C)</li>
<li>Consumer to Business (C2B)</li>
</ol>
    
